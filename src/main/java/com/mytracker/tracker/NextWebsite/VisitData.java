package com.mytracker.tracker.NextWebsite;

public class VisitData {
    private String location;
    private int secondsSpent;
    private String timeEntered;

    public String getLocation() {
        return this.location;
    }

    public void setLocation(String location) {
        this.location = location;
    }

    public int getSecondsSpent() {
        return this.secondsSpent;
    }

    public void setSecondsSpent(int secondsSpent) {
        this.secondsSpent = secondsSpent;
    }

    public String getTimeEntered() {
        return this.timeEntered;
    }

    public void setTimeEntered(String timeEntered) {
        this.timeEntered = timeEntered;
    }
}
